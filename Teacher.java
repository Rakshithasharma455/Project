    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String fullname = jTextField1.getText();
        String gender = (String)jComboBox1.getSelectedItem();
        String dateofbirth = jTextField2.getText();
        String mobileno = jTextField3.getText();
        String email = jTextField4.getText();
        String address = jTextField5.getText();
        String teaching = (String)jComboBox2.getSelectedItem();
        String branch = (String)jComboBox3.getSelectedItem();
        try
        {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into teacher values (?,?,?,?,?,?,?,?)");
            ps.setString(1,fullname);
            ps.setString(2,gender);
            ps.setString(3,dateofbirth);
            ps.setString(4,mobileno);
            ps.setString(5,email);
            ps.setString(6,address);
            ps.setString(7,teaching);
            ps.setString(8,branch);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully saved");
            setVisible(false);
            new Teacher().setVisible(true);
        }
            catch(Exception e)
                    {
                       JOptionPane.showMessageDialog(null, e);
                    }
    }//GEN-LAST:event_jButton2ActionPerformed

